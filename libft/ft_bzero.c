#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*mem_s;
	size_t	i;

	i = 0;
	mem_s = (char *)s;
	while (i < n)
	{
		mem_s[i] = '\0';
		i++;
	}
}
