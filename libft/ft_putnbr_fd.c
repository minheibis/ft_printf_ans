#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		i;
	char	c[10];

	i = 9;
	if (n < 0)
		write(fd, "-", 1);
	if (n == 0)
		write(fd, "0", 1);
	while (n)
	{
		if (n > 0)
			c[i] = '0' + n % 10;
		else
			c[i] = '0' - n % 10;
		n = n / 10;
		i--;
	}
	write(fd, &c[i + 1], 9 - i);
}
