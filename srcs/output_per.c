#include "ft_printf.h"

int		set_per(t_printf *p_t)
{
	if (!(p_t->o_tmp = (char *)malloc(sizeof(char) * 2)))
		return (-1);
	p_t->o_tmp[0] = '%';
	p_t->o_tmp[1] = '\0';
	return (0);
}
